  subroutine marbl_setup_local_tracers(column_kmt, marbl_tracer_indices, &
             tracers, tracer_local, zooplankton_local, autotroph_local, totalChl_local)

    !-----------------------------------------------------------------------
    !  create local copies of model tracers
    !  treat negative values as zero,  apply mask to local copies
    !-----------------------------------------------------------------------

    implicit none

    integer(int_kind)            , intent(in)  :: column_kmt
    type(marbl_tracer_index_type), intent(in)  :: marbl_tracer_indices
    real (r8)                    , intent(in)  :: tracers(:,:)
    real (r8)                    , intent(out) :: tracer_local(:,:)
    type(zooplankton_local_type) , intent(out) :: zooplankton_local(:,:)
    type(autotroph_local_type)   , intent(out) :: autotroph_local(:,:)
    real (r8)                    , intent(out) :: totalChl_local(:)

    !-----------------------------------------------------------------------
    !  local variables
    !-----------------------------------------------------------------------
    integer (int_kind) :: k                    ! vertical loop index
    integer (int_kind) :: zoo_ind, auto_ind, n ! tracer indices
    !-----------------------------------------------------------------------

    do k = 1, column_kmt
       tracer_local(:,k) = max(c0, tracers(1:size(tracer_local,1),k))
    end do

    do k = column_kmt+1, size(tracer_local,2)
       tracer_local(:,k) = c0
    end do

    !-----------------------------------------------------------------------
    ! populate zooplankton specific arrays from tracer_local
    !-----------------------------------------------------------------------

    do zoo_ind = 1, zooplankton_cnt
       n = marbl_tracer_indices%zoo_inds(zoo_ind)%C_ind
       zooplankton_local(zoo_ind,:)%C = tracer_local(n,:)
    end do

    !-----------------------------------------------------------------------
    ! populate autotroph specific arrays from tracer_local
    !-----------------------------------------------------------------------

    do auto_ind = 1, autotroph_cnt
       n = marbl_tracer_indices%auto_inds(auto_ind)%Chl_ind
       autotroph_local(auto_ind,:)%Chl = tracer_local(n,:)

       n = marbl_tracer_indices%auto_inds(auto_ind)%C_ind
       autotroph_local(auto_ind,:)%C = tracer_local(n,:)

       n = marbl_tracer_indices%auto_inds(auto_ind)%P_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%P = tracer_local(n,:)
       else
          autotroph_local(auto_ind,:)%P = &
               autotrophs(auto_ind)%Qp_fixed * autotroph_local(auto_ind,:)%C
       endif

       n = marbl_tracer_indices%auto_inds(auto_ind)%Fe_ind
       autotroph_local(auto_ind,:)%Fe = tracer_local(n,:)

       n = marbl_tracer_indices%auto_inds(auto_ind)%Si_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%Si = tracer_local(n,:)
       endif

       n = marbl_tracer_indices%auto_inds(auto_ind)%CaCO3_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%CaCO3 = tracer_local(n,:)
       endif

       ! Carbon isotope elements of autotroph_local
       n = marbl_tracer_indices%auto_inds(auto_ind)%C13_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%C13 = tracer_local(n,:)
       endif
       n = marbl_tracer_indices%auto_inds(auto_ind)%C14_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%C14 = tracer_local(n,:)
       endif

       n = marbl_tracer_indices%auto_inds(auto_ind)%Ca13CO3_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%Ca13CO3 = tracer_local(n,:)
       endif
       n = marbl_tracer_indices%auto_inds(auto_ind)%Ca14CO3_ind
       if (n > 0) then
          autotroph_local(auto_ind,:)%Ca14CO3 = tracer_local(n,:)
       endif
    end do

    ! autotroph consistency check
    call marbl_consistency_check_autotrophs(column_kmt, &
         marbl_tracer_indices, autotroph_local(:,1:column_kmt))

    ! set totalChl_local
    totalChl_local = sum(autotroph_local(:,:)%Chl, dim=1)

  end subroutine marbl_setup_local_tracers

  !***********************************************************************

  subroutine marbl_consistency_check_autotrophs(column_kmt,         &
             marbl_tracer_indices, autotroph_local)

    !-----------------------------------------------------------------------
    !  If any phyto box are zero, set others to zeros.
    !-----------------------------------------------------------------------

    implicit none

    integer(int_kind)          , intent(in)    :: column_kmt ! number of active model layers
    type(marbl_tracer_index_type) , intent(in) :: marbl_tracer_indices
    type(autotroph_local_type) , intent(inout) :: autotroph_local(autotroph_cnt, column_kmt)

    !-----------------------------------------------------------------------
    !  local variables
    !-----------------------------------------------------------------------
    integer (int_kind) :: auto_ind, k
    logical (log_kind) :: zero_mask
    !-----------------------------------------------------------------------

    do k = 1, column_kmt
       do auto_ind = 1, autotroph_cnt

          zero_mask = (autotroph_local(auto_ind,k)%Chl == c0 .or. &
                       autotroph_local(auto_ind,k)%C   == c0 .or. &
                       autotroph_local(auto_ind,k)%P   == c0 .or. &
                       autotroph_local(auto_ind,k)%Fe  == c0)

          if (marbl_tracer_indices%auto_inds(auto_ind)%Si_ind > 0) then
             zero_mask = zero_mask .or. autotroph_local(auto_ind,k)%Si == c0
          end if
          if (zero_mask) then
             autotroph_local(auto_ind,k)%Chl = c0
             autotroph_local(auto_ind,k)%C = c0
             autotroph_local(auto_ind,k)%P = c0
             autotroph_local(auto_ind,k)%Fe = c0
             if (marbl_tracer_indices%auto_inds(auto_ind)%Si_ind > 0) then
                autotroph_local(auto_ind,k)%Si = c0
             end if
             if (marbl_tracer_indices%auto_inds(auto_ind)%CaCO3_ind > 0) then
                autotroph_local(auto_ind,k)%CaCO3 = c0
             end if

             ! carbon isotope components of autotroph_local_type
             if (ciso_on) then
                autotroph_local(auto_ind,k)%C13 = c0
                autotroph_local(auto_ind,k)%C14 = c0

                if (marbl_tracer_indices%auto_inds(auto_ind)%Ca13CO3_ind > 0) then
                   autotroph_local(auto_ind,k)%Ca13CO3 = c0
                end if
                if (marbl_tracer_indices%auto_inds(auto_ind)%Ca14CO3_ind > 0) then
                   autotroph_local(auto_ind,k)%Ca14CO3 = c0
                end if
             end if
          end if

       end do
    end do

  end subroutine marbl_consistency_check_autotrophs

  !***********************************************************************

  subroutine marbl_compute_autotroph_elemental_ratios(autotroph_local, tracer_local, marbl_tracer_indices, &
             autotroph_secondary_species)

    use marbl_constants_mod, only : epsC
    use marbl_settings_mod , only : lvariable_PtoC
    use marbl_settings_mod , only : gQsi_0
    use marbl_settings_mod , only : gQsi_max
    use marbl_settings_mod , only : gQsi_min
    use marbl_settings_mod , only : gQ_Fe_kFe_thres
    use marbl_settings_mod , only : gQ_Si_kSi_thres
    use marbl_settings_mod , only : PquotaSlope, PquotaIntercept, PquotaMinNP

    implicit none

    type(autotroph_local_type)            , intent(in)    :: autotroph_local(:)
    real (r8)                             , intent(in)    :: tracer_local(:) ! local copies of model tracer concentrations
    type(marbl_tracer_index_type)         , intent(in)    :: marbl_tracer_indices
    type(autotroph_secondary_species_type), intent(inout) :: autotroph_secondary_species(:)

    !-----------------------------------------------------------------------
    !  local variables
    !-----------------------------------------------------------------------
    integer(int_kind) :: auto_ind
    !-----------------------------------------------------------------------

    associate(                                                 &
         PO4_loc    => tracer_local(marbl_tracer_indices%po4_ind),                  &
         Fe_loc     => tracer_local(marbl_tracer_indices%fe_ind),                   &
         SiO3_loc   => tracer_local(marbl_tracer_indices%sio3_ind),                 &
         auto_C     => autotroph_local(:)%C,                   &
         auto_P     => autotroph_local(:)%P,                   &
         auto_Chl   => autotroph_local(:)%Chl,                 &
         auto_Fe    => autotroph_local(:)%Fe,                  &
         auto_Si    => autotroph_local(:)%Si,                  &
         auto_CaCO3 => autotroph_local(:)%CaCO3,               &
         thetaC     => autotroph_secondary_species(:)%thetaC , & ! current Chl/C ratio (mg Chl/mmol C)
         QCaCO3     => autotroph_secondary_species(:)%QCaCO3 , & ! currenc CaCO3/C ratio (mmol CaCO3/mmol C)
         Qp         => autotroph_secondary_species(:)%Qp,      & ! current P/C ratio (mmol P/mmol C)
         gQp        => autotroph_secondary_species(:)%gQp,     & ! P/C for growth
         Qfe        => autotroph_secondary_species(:)%Qfe,     & ! current Fe/C ratio (mmol Fe/mmol C)
         gQfe       => autotroph_secondary_species(:)%gQfe,    & ! Fe/C for growth
         Qsi        => autotroph_secondary_species(:)%Qsi,     & ! current Si/C ratio (mmol Si/mmol C)
         gQsi       => autotroph_secondary_species(:)%gQsi     & ! diatom Si/C ratio for growth (new biomass)
         )

    !-----------------------------------------------------------------------
    !  set local variables, with incoming ratios
    !-----------------------------------------------------------------------

    do auto_ind = 1, autotroph_cnt
       thetaC(auto_ind) = auto_Chl(auto_ind) / (auto_C(auto_ind) + epsC)
       if (lvariable_PtoC) then
          Qp(auto_ind) = auto_P(auto_ind) / (auto_C(auto_ind) + epsC)
       else
          Qp(auto_ind) = autotrophs(auto_ind)%Qp_fixed
       endif
       Qfe(auto_ind) = auto_Fe(auto_ind) / (auto_C(auto_ind) + epsC)
       if (marbl_tracer_indices%auto_inds(auto_ind)%Si_ind > 0) then
          Qsi(auto_ind) = min(auto_Si(auto_ind) / (auto_C(auto_ind) + epsC), gQsi_max)
       endif
    end do

    !-----------------------------------------------------------------------
    !  DETERMINE NEW ELEMENTAL RATIOS FOR GROWTH (NEW BIOMASS)
    !  Modify these initial ratios under low ambient iron conditions
    !  Modify the initial si/C ratio under low ambient Si conditions
    !-----------------------------------------------------------------------

    do auto_ind = 1, autotroph_cnt
       if (lvariable_PtoC) then
          !-----------------------------------------------------------------------
          !-- Calculate Qp for new growth based on Galbraith and Martiny (2015), with min. N/P
          ! - 14= 0.00976801, 14.5 = 0.00944239 15= 0.00911677 15.5=0.00882272 16= 0.00854701
          ! - std intercept 6.0 = 166.66maxCP, 5.26=190, 4.0 = 250, 3.0 = 333.33
          !-----------------------------------------------------------------------
          gQp(auto_ind) = min((((PquotaSlope * PO4_loc) + PquotaIntercept) * 0.001_r8), PquotaMinNP)
       else
          ! group-specific fixed P:C ratios
          gQp(auto_ind) = autotrophs(auto_ind)%Qp_fixed
       end if

       !  Uncomment this line to use modified, fixed Redfield (C/N/P 117/16/1) stoichiomdetry
       !      gQp(auto_ind) = 0.00854701_r8      ! fixed Redfield C/N/P

       gQfe(auto_ind) = autotrophs(auto_ind)%gQfe_0
       if (Fe_loc < gQ_Fe_kFe_thres * autotrophs(auto_ind)%kFe) then
          gQfe(auto_ind) = &
               max((gQfe(auto_ind) * Fe_loc / (gQ_Fe_kFe_thres * autotrophs(auto_ind)%kFe)), &
               autotrophs(auto_ind)%gQfe_min)
       end if

       if (marbl_tracer_indices%auto_inds(auto_ind)%Si_ind > 0) then
          gQsi(auto_ind) = gQsi_0
          if ((Fe_loc < gQ_Fe_kFe_thres * autotrophs(auto_ind)%kFe) .and. &
               (Fe_loc > c0) .and. &
               (SiO3_loc > (gQ_Si_kSi_thres * autotrophs(auto_ind)%kSiO3))) then
             gQsi(auto_ind) = min((gQsi(auto_ind) * gQ_Fe_kFe_thres * autotrophs(auto_ind)%kFe / Fe_loc), gQsi_max)
          end if

          if (Fe_loc == c0) then
             gQsi(auto_ind) = gQsi_max
          end if

          if (SiO3_loc < (gQ_Si_kSi_thres * autotrophs(auto_ind)%kSiO3)) then
             gQsi(auto_ind) = max((gQsi(auto_ind) * SiO3_loc / (gQ_Si_kSi_thres * autotrophs(auto_ind)%kSiO3)), &
                  gQsi_min)
          end if
       endif

       !-----------------------------------------------------------------------
       !  QCaCO3 is the percentage of sp organic matter which is associated
       !  with coccolithophores
       !-----------------------------------------------------------------------

       if (marbl_tracer_indices%auto_inds(auto_ind)%CaCO3_ind > 0) then
          QCaCO3(auto_ind) = auto_CaCO3(auto_ind) / (auto_C(auto_ind) + epsC)
          if (QCaCO3(auto_ind) > QCaCO3_max) then
             QCaCO3(auto_ind) = QCaCO3_max
          end if
       end if
    end do
    end associate
  end subroutine marbl_compute_autotroph_elemental_ratios

